<span class="title_blue">BLOK 4.RATA-RATA HASIL TANGKAPAN IKAN YANG <br/>DIDARATKAN PER HARI PENDARATAN MENURUT <br/>JENIS PERAHU/KAPAL</span><br/>
<p>
<span>A. PERAHU TANPA MOTOR (kolom 3 dan 4)</span><br/>
Rincian A ( 1 s.d. 4 ) dikhususkan untuk perahu tanpa motor
<table>
    <tr>
        <td>Rincian 1 :</td>
        <td>Isikan banyaknya hari pendaratan ikan selama triwulan laporan dari jenis perahu khusus Perahu tanpa motor.Hari Pendaratan adalah hari dimana ada perahu yang mendaratkan ikan (khusus perahu tanpa motor)
Jika dalam 1 triwulan setiap hari ada perahu tanpa motor yang mendaratan ikan maka banyak hari pendaratan dalam 1 triwulan = 90 - 92 hari pendaratan</td>
    </tr>
    <tr>
        <td>Rincian 2 :</td>
        <td>Isikan rata-rata banyaknya perahu yang mendaratkan ikan pada setiap hari pendaratan khusus perahu tanpa motor</td>
    </tr>
    <tr>
        <td>Rincian 3 (kol.3) :</td>
        <td>Isikan rata-rata volume ikan yang yang didaratkan per hari pendaratan dari seluruh perahu tanpa motor dalam satuan kilogram (kg).</td>
    </tr>
    <tr>
        <td>Rincian 4 (kol.4) :</td>
        <td>Isikan rata-rata nilai ikan yang yang didaratkan per hari pendaratan dari seluruh perahu tanpa motor dalam satuan rupiah (Rp)</td>
    </tr>
</table>
